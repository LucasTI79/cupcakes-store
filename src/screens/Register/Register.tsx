import auth from '@react-native-firebase/auth';
import { useNavigation } from '@react-navigation/native';
import { useState } from 'react';
import { ToastAndroid } from 'react-native';
import { KeyboardAwareScrollView } from 'react-native-keyboard-aware-scroll-view';

import { Button } from '@components/controllers/buttons/Button';
import { TextInput } from '@components/controllers/inputs/TextInput';

import { Container, FooterLink, FooterText, FooterView } from './styles';

export function Register() {
  const [fullName, setFullName] = useState('');

  const [email, setEmail] = useState('');

  const [password, setPassword] = useState('');

  const [confirmPassword, setConfirmPassword] = useState('');

  const navigate = useNavigation();

  function handleRegister() {
    auth()
      .createUserWithEmailAndPassword(email, password)

      .catch((error) => {
        ToastAndroid.show(error.message, ToastAndroid.SHORT);
      });
  }

  function handleNavigateToLogin() {
    navigate.navigate('login');
  }

  return (
    <KeyboardAwareScrollView
      style={{ flex: 1, width: '100%', height: '100%' }}
      keyboardShouldPersistTaps="always"
    >
      <Container>
        {/* <Image































































































































































































































































































































































































































































































































              style={styles.logo}































































































































































































































































































































































































































































































































              source={require('../../../assets/icon.png')}































































































































































































































































































































































































































































































































          /> */}

        <TextInput
          placeholder="Full Name"
          placeholderTextColor="#aaaaaa"
          onChangeText={(text) => setFullName(text)}
          value={fullName}
          underlineColorAndroid="transparent"
          autoCapitalize="none"
        />

        <TextInput
          placeholder="E-mail"
          placeholderTextColor="#aaaaaa"
          onChangeText={(text) => setEmail(text)}
          value={email}
          underlineColorAndroid="transparent"
          autoCapitalize="none"
        />

        <TextInput
          placeholderTextColor="#aaaaaa"
          secureTextEntry
          placeholder="Password"
          onChangeText={(text) => setPassword(text)}
          value={password}
          underlineColorAndroid="transparent"
          autoCapitalize="none"
        />

        <TextInput
          placeholderTextColor="#aaaaaa"
          secureTextEntry
          placeholder="Confirm Password"
          onChangeText={(text) => setConfirmPassword(text)}
          value={confirmPassword}
          underlineColorAndroid="transparent"
          autoCapitalize="none"
        />

        <Button title="Create account" onPress={handleRegister} />

        <FooterView>
          <FooterText>
            {' '}
            Already got an account?{' '}
            <FooterLink onPress={handleNavigateToLogin}>Login</FooterLink>
          </FooterText>
        </FooterView>
      </Container>
    </KeyboardAwareScrollView>
  );
}
